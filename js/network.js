  let issue = document.getElementById("issue")
  function getIssue() {
      let issueVal = document.getElementById("issue").value;
      let solve = document.getElementById("solve");
      if (issueVal === "0") {
          solve.innerHTML = `
          <h1>اختر احدى المشاكل من القائمة المنسدلة</h1>
          <img src="../images/terminal/terminal.gif" alt="terminal" width="550px" height="240px">
      `;
      }
      // Create symptoms selection HTML
      let symptomsHTML = `
          <h2>هل تواجه هذه الأعراض؟</h2>
          <div class="solve-column">
              <h3>الأعراض:</h3>
              <ul>
                  ${computerFaults[issueVal].symptoms.map(symptom => `<li>${symptom}</li>`).join('')}
              </ul>
              <select id="symptomsCheck" onchange="checkSymptoms(${issueVal})">
                  <option value="">-- اختر --</option>
                  <option value="yes">نعم</option>
                  <option value="no">لا</option>
              </select>
          </div>
      `;
  
      solve.innerHTML = symptomsHTML;
  }
  
  function checkSymptoms(issueVal) {
      let symptomsCheck = document.getElementById("symptomsCheck").value;
      let solve = document.getElementById("solve");
  
      if (symptomsCheck === "yes") {
          // Show causes and solutions if symptoms match
          solve.innerHTML = `
              <h2>تشخيص المشكلة</h2>
              <div class="solve-column">
                  <h3>الأسباب المحتملة:</h3>
                  <ul>
                      ${computerFaults[issueVal].causes.map(cause => `<li>${cause}</li>`).join('')}
                  </ul>
              </div>
              <div class="solve-column">
                  <h3>الحلول المقترحة:</h3>
                  <ul>
                      ${computerFaults[issueVal].solutions.map(solution => `<li>${solution}</li>`).join('')}
                  </ul>
              </div>
          `;
      } else if (symptomsCheck === "no") {
          // New response for "no"
          solve.innerHTML = `
              <h2>تنبيه</h2>
              <div class="solve-column" style="text-align: center;">
                  <p style="color: var(--secondary-color); font-size: 24px; margin: 20px 0;">
                      يبدو أن المشكلة غير واضحة، برجاء التأكد من الأعراض ثم إعادة المحاولة
                  </p>
                <img src="../images/error.gif" alt="No Symptom Image" width="300px" height="200px">
              </div>
          `;
      }
  }
  
  issue.addEventListener("change", getIssue);